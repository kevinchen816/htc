<ul>
    <li>On each event, your camera will upload a photo to the portal.</li>
    <li>You determine your upload cost in points using the chart below.</li>
    <li>After reviewing the uploaded photos, you may request a larger size
        photo (Photo Mode) or the playable video (Video Mode).</li>
    <li>By only uploading a photo for each event, you will know the cost ahead of time, and avoid wasted points and battery power.</li>
    <li>As you browse photos in the gallery, the points cost is enumerated in the caption.</li>
    <li>The video upload cost is shown if the camera is in Video Mode.</li>
    <li>You will know all points costs before you use the points putting you in complete control.</li>
</ul>

